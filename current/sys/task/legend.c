int check_task_item1(object who, string name, int count)
{
	int i,size,amount;
	object *inv,item;	
	inv = all_inventory(who, 1, MAX_CARRY);
	
	if( !objectp( item = present(name, who, 1, MAX_CARRY) )  )
	{
		send_user(who,"%c%s",'!',"你的"+name+"呢?");
		return 0;	
	}			
	for(i=0,size=sizeof(inv);i<size;i++)
	{
		if ( !objectp(inv[i]) )
			continue;
		if ( inv[i]->get_name() != name  )
			continue;
		if ( inv[i]->is_combined() )
			amount += inv[i]->get_amount();		
		else
			amount ++;
	}
	if ( amount < count )
	{
		send_user(who,"%c%s",'!',"数量不够吧?");
		return 0;	
	}
	amount = count;
	for(i=0,size=sizeof(inv);i<size;i++)
	{
		if ( !objectp(inv[i]) )
			continue;
		if ( inv[i]->get_name() != name )
			continue;
		if ( !inv[i]->is_combined() )
		{
			inv[i]->remove_from_user();
			destruct(inv[i]);
			amount--;
			if ( amount <= 0 )
				break;
			continue;
		}
		if ( inv[i]->get_amount() <= amount )
		{
			amount -= inv[i]->get_amount();
			inv[i]->remove_from_user();
			destruct(inv[i]);
		}
		else
		{
			inv[i]->set_amount(inv[i]->get_amount()-amount);
			amount = 0 ;			
		}
		if ( amount <= 0 )
			break;
	}
	return 1;	
}
