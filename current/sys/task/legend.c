//检查并删除任务物品,返回1成功
int check_task_item(object who, string name, int count)
{
	int i,size,amount;
	object *inv,item;	
	inv = all_inventory(who, 1, MAX_CARRY);
	
	if( !objectp( item = present(name, who, 1, MAX_CARRY) ) || item->is_legend_item() != 1 )
	{
		send_user(who,"%c%s",'!',"你的"+name+"呢?");
		return 0;	
	}			
	for(i=0,size=sizeof(inv);i<size;i++)
	{
		if ( !objectp(inv[i]) )
			continue;
		if ( inv[i]->get_name() != name || inv[i]->is_legend_item() != 1 )
			continue;
		if ( inv[i]->is_combined() )
			amount += inv[i]->get_amount();		
		else
			amount ++;
	}
	if ( amount < count )
	{
		send_user(who,"%c%s",'!',"数量不够吧?");
		return 0;	
	}
	for(i=0,size=sizeof(inv);i<size;i++)
	{
		if ( !objectp(inv[i]) )
			continue;
		if ( inv[i]->get_name() != name || inv[i]->is_legend_item() != 1 )
			continue;
		inv[i]->remove_from_user();
		destruct(inv[i]);
	}
	return 1;	
}
