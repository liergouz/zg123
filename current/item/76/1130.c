// 函数：构造处理
void create()
{
        set_name("诛仙剑");
        set_picid_1(7623);
        set_picid_2(7623);
        set_unit("柄");

        set_level(130);
        set_value(72000);
        set_max_lasting(10599);
        set("ap", 205);
        set("!%", 10);
        set("cp", 270);
        set("c!%", 30);
        set("spec.mp", 220);
        set("spec.hp*", 2);
        set("spec.mp*", 8);

        setup();

        set_weapon_type(SWORD);
}

// 函数：获取武器级别
int get_weapon_level() { return 34; }

// 函数：获取描述
